/**
 *
 * Coercion in JS is the conversion of build in type.
 * Two types of coercion- implicit coercion & explicit coercion
 */

//implict conversion of build in types by JS engine
console.log(2 + "10"); // output is a string

// explicit coercion
/**
 * here, we have passed string to the Number() method but returns a number 10
 */
console.log(Number("10")); // output is a number 10
